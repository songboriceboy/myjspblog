package model;

public class hits {
	
	private int id;
	private int hitTimes;  
    private String date;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHitTimes() {
		return hitTimes;
	}
	public void setHitTimes(int hitTimes) {
		this.hitTimes = hitTimes;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	

}
